# Тестовий рядок
test_str = " hello world! "
print(f"Оригінал: '{test_str}'")

# 1. strip(видаляє пробіли або інші вказані символи з початку і кінця рядка)
print(f"strip: '{test_str.strip()}'")

# 2. capitalize(робить першу літеру рядка великою, а всі інші — малими.)
print(f"capitalize: '{test_str.capitalize()}'")

# 3. title(робить кожне слово в рядку з великої літери.)
print(f"title: '{test_str.title()}'")

# 4. upper(переводить всі символи рядка в верхній регістр.)
print(f"upper: '{test_str.upper()}'")

# 5. lower(переводить всі символи рядка в нижній регістр.)
print(f"lower: '{test_str.lower()}'")
